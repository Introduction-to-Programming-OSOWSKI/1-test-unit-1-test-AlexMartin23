def name(n):
    return n

def favorites(favCol, favNum):
    return "Your favorite color is " + favCol + " and your favorite number is " + favNum

def bigAdd(x, y, z, a, b):
    return x + y + z + a + b

def fourOperations(x, y, z, a, b):
    return (x+y)-(z*a)/b


